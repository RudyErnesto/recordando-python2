print(3 > 2)     # True, por que 3 es mayor a 2
print(3 >= 2)    # True, por que 3 es mayor a 2
print(3 < 2)     # False,  por que 3 es mayor a 2
print(2 < 3)     # True, por que 2 es menor a 3
print(2 <= 3)    # True, por que 2 es menor a 3
print(3 == 2)    # False, por que 3 es no igual a 2
print(3 != 2)    # True, por que 3 es no igual a 2
print(len('mango') == len('avocado'))  # False
print(len('mango') != len('avocado'))  # True
print(len('mango') < len('avocado'))   # True
print(len('milk') != len('meat'))      # False
print(len('milk') == len('meat'))      # True
print(len('tomato') == len('potato'))  # True
print(len('python') > len('dragon'))   # False

#otras comparaciones en python
print('1 is 1', 1 is 1)                   # True - porque el valor es el mismo
print('1 is not 2', 1 is not 2)           # True - porque 1 no es 2
print('A in Amasando', 'A' in 'Amasando') # True - A se encuentra en el string
print('B in Amasando', 'B' in 'Amasando') # False -  no hay  B
print('codeando' in 'codeando for all') # True - porque codeando for all tiene la palabra codeando
print('a in an:', 'a' in 'an')      # True
print('4 is 2 ** 2:', 4 is 2 ** 2)   # True